def detect_packet_types(log = true)
  if log
    puts ""
    puts "# Detecting types of #{packet_types.count} different packets..."
  end

  packet_types.each do |packet|
    type = GprsC.packet_type(packet)

    if log
      puts "Packet: #{packet}"
      puts "Packet Type: #{type}"
    end
  end
end

def parse_commands(log = true)
  if log
    puts ""
    puts "# Parsing single command packet..."
  end

  commands.each do |packet|
    command = GprsC.packet_parse(packet, true)
    if log
      puts command
      puts ""
    end
  end
end
